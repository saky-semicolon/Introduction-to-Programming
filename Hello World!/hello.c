// Simple C program to display "Hello World"

// Header file for input output functions
#include <stdio.h>

// This program is in a header file called <stdio.h>
// "stdio" stands for "standard input output" and h indicates that this is a header file that contains scanf and printf, these built-in functions and can take input from the user and print the output.

// main function - first bracket means this is a function.
// where the execution of program begins
int main() //int means integer. It's a keyword.

{ // Starting of the Program.
    // prints hello world
    printf("Hello World!");     //printf is a built in function that prints the output to the screen.

    return 0; //it's a keyword.
} //ending of the Program.